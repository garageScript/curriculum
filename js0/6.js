/**
 * Write a function called solution that
 * Takes in 2 numbers, return true if the
 *    first number is greater than the second
 *    and false otherwise
 * @param {number} num1
 * @param {number} num2
 * @returns {boolean}
 */

const solution = (num1, num2) => num1 > num2;

module.exports = {
  solution,
};
