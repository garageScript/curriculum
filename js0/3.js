/**
 * Write a function called solution that
 * takes in a number and returns true if that number is greater than 5, false otherwise
 * @param {number} num
 * @returns {boolean}
 */

const solution = (num) => {
  if (num > 5) {
    return true
  }
  else {
    return false
  }
}

module.exports = {
  solution
}
